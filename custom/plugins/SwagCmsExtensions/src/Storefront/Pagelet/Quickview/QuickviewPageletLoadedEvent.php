namespace Swag\CmsExtensions\Storefront\Pagelet\Quickview;

use Shopware\Core\System\SalesChannel\SalesChannelContext;
use Shopware\Storefront\Pagelet\PageletLoadedEvent;
use Symfony\Component\HttpFoundation\Request;

class QuickviewPageletLoadedEvent extends PageletLoadedEvent
{
    /**
     * @var QuickviewPagelet
     */
    protected $pagelet;

    public function __construct(QuickviewPagelet $pagelet, SalesChannelContext $salesChannelContext, Request $request)
    {
        $this->pagelet = $pagelet;
        parent::__construct($salesChannelContext, $request);
    }

    public function getPagelet(): QuickviewPagelet
    {
        return $this->pagelet;
    }
}
